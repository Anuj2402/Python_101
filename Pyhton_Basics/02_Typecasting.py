'''
# Typecasting is the process of converting a variable from one type to another.
# For example, converting an integer to a string, or a string to a float.



'''


# Example of typecasting
x = 5  # integer
y = str(x)  # typecast to string
print(y)  # Output: '5'

a = "123"  # string
b = int(a)  # typecast to integer
print(b)  # Output: 123

c = "3.14"  # string
d = float(c)  # typecast to float
print(d)  # Output: 3.14
